public class Variable {
    public static void main(String[] args) {
        int a =10;
        int b=20;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        String name="jay";
        System.out.println("name = " + name);
        int c=50;
        System.out.println("c = " + c);
        int d=30;
        System.out.println("d = " + d);
    }
}
