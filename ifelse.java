public class ifelse {
    public static void main(String[] args) {
        int a=55;
        int b=45;
        if(a>b) {
            System.out.println("a is greater than b");
        } else if (a<b) {
            System.out.println("b is greater than a");
        } else {
            System.out.println("a is equal to b");
        }
    }
}
